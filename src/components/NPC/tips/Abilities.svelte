<Tooltip>
    <p>Abilities are actions, or enablers that allow you to do something specific, or make specific tasks easier.</p>
    <p>Abilities typically cost points to apply, for example [1S] means it costs 1 speed point to apply from your speed pool</p>
    <p>The following are codes for each ability</p>
    <ul>
        <li><span>A</span>: An action. You can perform this on your turn. Hover over the icon to see the action does</li>
        <li><span>E</span>: An enabler. Not an action in itself it either allows you to do something others can't, or makes a task easier that others would find difficult. May still have a cost and may be time limited</li>
        <li><span>I</span>: An inability. This makes certain tasks for you harder, increasing the difficulty by 1 level</li>
        <li><span>S</span>: A skill. Something your character is good at. This makes tasks related to that skill easier. This is innate, meaning it costs no points to use and doesn't need to be activated or "used"</li>
        <li><span>D</span>: This ability is a result of the descriptor you picked for your character for example "hardy", "clever", "mystical" etc.</li>
        <li><span>C</span>: A cypher, a one use ability that you gain throughout the game</li>
    </ul>
</Tooltip>