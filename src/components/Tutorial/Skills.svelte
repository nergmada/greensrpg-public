<h2>Skills</h2>
<p>Skills are something your character is either "trained" or "specialised" in. If they're trained in a skill it makes an action related to that skill easier by 1 level. If they're specialised it makes it easier by two levels.</p>
<p>Skills do not stack, so the most a skill can lower the difficulty by is 2 levels</p>
<p>The skills your character has are listed under the abilities on your player card, and marked with an (S)</p>
