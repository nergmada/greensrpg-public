<h2>Effort</h2>
<p>Finally you can make a task easier simply by trying harder.</p>
<p>Every action you take gets classified into one of three categories, Might, Speed or Intellect. Might tasks are those involving raw strength. Speed are athletic tasks that involve agility and stamina. Intellect tasks are those involving deduction or reasoning</p>
<p>When you apply effort you spend points from your Might, Speed, or Intellect pool, depending on the type of action. 3 points for the first level of effort, and 2 points per level of effort after that</p>
<p>Your maximum level of effort is shown on your player card</p>
