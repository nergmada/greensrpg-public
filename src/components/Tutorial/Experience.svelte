<h2>Experience & Intrusions</h2>
<p>Occasionally, the GM will perform an "intrusion" which is a twist in the story during a player's turn. For example you drop your sword</p>
<p>When this happens you get 1 EXP point, and you can give 1 more EXP point to another team member</p>
<p>EXP points can later be used for your own advantageous player intrusions of your own design.</p>
