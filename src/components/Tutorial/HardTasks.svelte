<h2>Completing harder tasks</h2>
<p>The target number for the dice is 3 times the difficulty level. E.g. a level 3 task requires a roll of 9 or above to succeed.</p>
<p>This makes some tasks impossible. Any task level 7 or above is impossible for a regular person because you'd need to roll an 21</p>
