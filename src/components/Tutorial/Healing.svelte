<h2>Healing & Recovering</h2>
<p>For one of your Actions, you can choose to recover. However, recovering moves time forward. Which can make accomplishing your mission before it's too late difficult</p>
<img src={clock} alt="clock" />
<p>The first time you recover, it takes seconds, the next time 15 minutes. Then an hour, then 8 hours. As a result, during combat you can only recover if it's your first time</p>
<p>When you recover, you roll a 6 sided dice. You can then decide what pools you want to put the score into. You can distribute these points however you like.</p>
