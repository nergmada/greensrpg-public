<h2>Easing</h2>
<p>A hard task can be made easier through "easing". Easing can be achieved in a number of ways</p>
<ul>
    <li>Having skills that would make this task easier</li>
    <li>Having equipment or an asset that would make this task easier</li>
    <li>Putting more effort into the task</li>
</ul>
<p>Easing lowers the level of the task and in doing so, the target number you need to roll.</p>
