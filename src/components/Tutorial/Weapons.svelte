<h2>Weapons & Damage</h2>
<p>Some players will have Weapons. See your inventory for items marked (W).</p>
<p>Your damage is determined by kind of weapon you use. Light (Lgt) weapons do 2 damage if you succeed in your attack roll. Medium (Med) weapons do 4 damage. Heavy (Hvy) do 6 damage.</p>
<p>Any kind of hand-to-hand combat is classified as a light weapon. Importantly, using a light weapon lowers the difficulty level of attacking by 1 automatically.</p>
