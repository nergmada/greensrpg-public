<h2>More on Equipment</h2>
<p>Assets are special form of equipment suitable only for one task. E.g. a key for a specific lock, or maybe a ledger of someone's misdeeds you use to manipulate them</p>
<p>Both can have intrinsic value to certain people, so may also be traded for other items. Value is denoted by (E) for expensive, (M) for moderate, (I) for inexpensive</p>
