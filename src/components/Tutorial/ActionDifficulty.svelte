<h2>Action Difficulty</h2>
<p>Action difficulty is the main thing the Game Master (GM) determines. Essentially, is the action you want to do really easy, or really hard?</p>
<p>This is done by giving it a difficulty level between 0 and 10</p>
<p>A difficulty of level 0, means anyone can do it all the time. A lot of actions are level 0, walking, looking, picking items up etc.</p>
<p>When a task is greater in level than level 0, we need to roll a dice, a 20 sided dice specifically. Dices are provided at the bottom of the game screen</p>
