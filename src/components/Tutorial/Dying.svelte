<h2>Player Damage & Dying</h2>
<p>Players don't have health points. Instead an attack by an NPC will deduct from one of your Might, Speed or Intellect pools. Usually Might.</p>
<p>Once you run out of points in one of your pools, you are considered injuried and everything gets 1 level more difficult. If you are out of might points, we start deducting from Speed then Intellect</p>
<p>If all three of your pools run out, you die.</p>
