<h2>Equipment</h2>
<p>Equipment and assets are items in your inventory that make an action easier. Each item can only make a task one level easier</p>
<p>At most two pieces of equipment or assets can be applied, lowering the difficulty level by a maximum of 2</p>
<p>Equipment or assets might be used in novel ways, for example, using a rope as an explosive fuse, rather than to climb. Your inventory is on your player card.</p>
