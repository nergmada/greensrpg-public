<h2>Cyphers</h2>
<p>In game you may get a cypher from time to time. This will be marked by a (C) next to a listing on either you inventory or you abilities list.</p>
<p>A cypher is a powerful one use object or ability that allows you to affect the over all story. Its description (the "i" icon next to it) will tell you what it can do</p>
<p>Cyphers are designed to be used.</p>
