<h2>Inabilities & Other Hinderances</h2>
<p>Certain players may have Inabilities, listed on their abilities section of their player card and marked with an (I)</p>
<p>Inabilities increase the difficulty of any task they relate to by 1 level.</p>
<p>Other hinderances can also occur. For example, if you're knocked down, it's dark, hot, cold or some other environmental limitation. This will be factored by the GM when coming up with the initial difficulty level</p>
